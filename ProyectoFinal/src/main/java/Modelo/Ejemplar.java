package Modelo;

/**
 *
 * @author franc
 */
public class Ejemplar {
    
    private int idEjemplar;
    private String tituloEjemplar;
    private String autorEjemplar;
    private String generoEjemplar;
    private String editorialEjemplar;
    private boolean estadoPrestamo;

    public int getIdEjemplar() {
        return idEjemplar;
    }

    public void setIdEjemplar(int idEjemplar) {
        this.idEjemplar = idEjemplar;
    }

    public String getTituloEjemplar() {
        return tituloEjemplar;
    }

    public void setTituloEjemplar(String tituloEjemplar) {
        this.tituloEjemplar = tituloEjemplar;
    }

    public String getAutorEjemplar() {
        return autorEjemplar;
    }

    public void setAutorEjemplar(String autorEjemplar) {
        this.autorEjemplar = autorEjemplar;
    }

    public String getGeneroEjemplar() {
        return generoEjemplar;
    }

    public void setGeneroEjemplar(String generoEjemplar) {
        this.generoEjemplar = generoEjemplar;
    }

    public String getEditorialEjemplar() {
        return editorialEjemplar;
    }

    public void setEditorialEjemplar(String editorialEjemplar) {
        this.editorialEjemplar = editorialEjemplar;
    }

    public boolean isEstadoPrestamo() {
        return estadoPrestamo;
    }

    public void setEstadoPrestamo(boolean estadoPrestamo) {
        this.estadoPrestamo = estadoPrestamo;
    }
    
    
    
    
}
